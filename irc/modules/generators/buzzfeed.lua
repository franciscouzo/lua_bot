	local words = utils.deepcopy(buzzfeed_words)
	local formula = utils.random_choice(formulas)
	local ret = {}
	for _, word in ipairs(formula) do
		if type(word) == "table" then
			word = utils.random_choice(word)
		end
		if words[word] then
			table.insert(ret, table.remove(words[word], math.random(#words[word])))
		else
			table.insert(ret, word)
		end
	end
	return utils.strip(table.concat(ret, " "))
end